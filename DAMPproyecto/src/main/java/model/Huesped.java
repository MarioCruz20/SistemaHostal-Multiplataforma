import java.sql.Date;

public class Huesped {

    private Integer id;
    private String nombre;
    private Date fechaNacimiento;   // usar java.sql.Date para JDBC simple
    private String direccion;
    private String sexo;            // 'M','F','O'
    private String telefono;
    private String dui;
    private String email;
    private Integer visitasPrevias;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Date getFechaNacimiento() {
        return fechaNacimiento;
    }

    public void setFechaNacimiento(Date fechaNacimiento) {
        this.fechaNacimiento = fechaNacimiento;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getDui() {
        return dui;
    }

    public void setDui(String dui) {
        this.dui = dui;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Integer getVisitasPrevias() {
        return visitasPrevias;
    }

    public void setVisitasPrevias(Integer visitasPrevias) {
        this.visitasPrevias = visitasPrevias;
    }
}
